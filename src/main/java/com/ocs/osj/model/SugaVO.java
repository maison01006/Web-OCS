package com.ocs.osj.model;

public class SugaVO {
	private String sugaId;
	private String classNum;
	private String sugaNameKor;
	private String sugaNameEng;
	private String computeName;
	private int unitCost;
	private String relativeValueP;
	private String payNonpay;
	private String applyDate;

	public String getSugaId() {
		return sugaId;
	}
	public void setSugaId(String sugaId) {
		this.sugaId = sugaId;
	}
	public String getClassNum() {
		return classNum;
	}
	public void setClassNum(String classNum) {
		this.classNum = classNum;
	}
	public String getSugaNameKor() {
		return sugaNameKor;
	}
	public void setSugaNameKor(String sugaNameKor) {
		this.sugaNameKor = sugaNameKor;
	}
	public String getSugaNameEng() {
		return sugaNameEng;
	}
	public void setSugaNameEng(String sugaNameEng) {
		this.sugaNameEng = sugaNameEng;
	}
	public String getComputeName() {
		return computeName;
	}
	public void setComputeName(String computeName) {
		this.computeName = computeName;
	}
	public int getUnitCost() {
		return unitCost;
	}
	public void setUnitCost(int unitCost) {
		this.unitCost = unitCost;
	}
	public String getRelativeValueP() {
		return relativeValueP;
	}
	public void setRelativeValueP(String relativeValueP) {
		this.relativeValueP = relativeValueP;
	}
	public String getPayNonpay() {
		return payNonpay;
	}
	public void setPayNonpay(String payNonpay) {
		this.payNonpay = payNonpay;
	}
	public String getApplyDate() {
		return applyDate;
	}
	public void setApplyDate(String applyDate) {
		this.applyDate = applyDate;
	}
}
