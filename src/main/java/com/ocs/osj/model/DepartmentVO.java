package com.ocs.osj.model;

public class DepartmentVO {
	private int id;
	private String major;
	private String majorAbb;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public String getMajorAbb() {
		return majorAbb;
	}
	public void setMajorAbb(String majorAbb) {
		this.majorAbb = majorAbb;
	}
}
