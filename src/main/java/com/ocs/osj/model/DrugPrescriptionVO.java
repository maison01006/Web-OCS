package com.ocs.osj.model;

public class DrugPrescriptionVO {
	private int drugPresId;
	private int receiveId;
	private int drugId;
	private int days;
	private int oneDosage;
	private int dayDosage;
	public int getDrugPresId() {
		return drugPresId;
	}
	public void setDrugPresId(int drugPresId) {
		this.drugPresId = drugPresId;
	}
	public int getReceiveId() {
		return receiveId;
	}
	public void setReceiveId(int receiveId) {
		this.receiveId = receiveId;
	}
	public int getDrugId() {
		return drugId;
	}
	public void setDrugId(int drugId) {
		this.drugId = drugId;
	}
	public int getDays() {
		return days;
	}
	public void setDays(int days) {
		this.days = days;
	}
	public int getOneDosage() {
		return oneDosage;
	}
	public void setOneDosage(int oneDosage) {
		this.oneDosage = oneDosage;
	}
	public int getDayDosage() {
		return dayDosage;
	}
	public void setDayDosage(int dayDosage) {
		this.dayDosage = dayDosage;
	}
	
	
}
